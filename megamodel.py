extra_cols_to_drop = ['Mid']  # add more if needed
for col in extra_cols_to_drop:
    if col in df.columns:
        df.drop(columns=[col], inplace=True)

##############################################################################
# 2. CREATE A DUMMY ROW FOR TOMORROW, SO THE LAST DAY GETS LABELED
##############################################################################
if len(df) == 0:
    sys.exit("No rows in your dataset! Please provide data.")

actual_final_date = df.index[-1]  # This is now 2025-01-09
tomorrow_after_final = actual_final_date + pd.Timedelta(days=1)

# Append a dummy row so that final date can be labeled via shift(-1)
dummy_row = pd.DataFrame({
    'Open':  [np.nan],
    'High':  [np.nan],
    'Low':   [np.nan],
    'Close': [np.nan],
}, index=[tomorrow_after_final])
df = pd.concat([df, dummy_row])

##############################################################################
# 3. DEFINE NEXT-DAY LABEL (BULLISH vs. BEARISH) USING SHIFT
##############################################################################
df['next_open'] = df['Open'].shift(-1)
df['next_close'] = df['Close'].shift(-1)

# target = 1 if (next_close > next_open), else 0
df['target'] = np.where(
    (df['next_open'].notna()) & (df['next_close'].notna()),
    (df['next_close'] > df['next_open']).astype(int),
    np.nan
)

# Remove the dummy row from the actual DataFrame
df = df.loc[df.index <= actual_final_date]

##############################################################################
# 4. SPLIT LABELED ROWS (TRAIN) VS. ANYTHING UNLABELED
##############################################################################
df.dropna(subset=['Open','High','Low','Close','target'], inplace=True)

final_labeled_date = df.index[-1]
print(f"** Our final labeled date is: {final_labeled_date.strftime('%Y-%m-%d')} **")

df_labeled = df[df['target'].notna()].copy()
if len(df_labeled) < 1:
    sys.exit("ERROR: No labeled rows found. Provide more data or fix your dataset.")

feature_cols = ['Open', 'High', 'Low', 'Close']  # no indicators
X_full = df_labeled[feature_cols]
y_full = df_labeled['target']

print(f"Training on {len(X_full)} rows, from {df_labeled.index[0].strftime('%Y-%m-%d')} "
      f"to {df_labeled.index[-1].strftime('%Y-%m-%d')}.")

##############################################################################
# 5. TRAIN MULTIPLE BASE MODELS (RandomForest, XGBoost, LightGBM, CatBoost)
##############################################################################
print("Training base models on the ENTIRE labeled dataset...")

# Random Forest
rf_model = RandomForestClassifier(n_estimators=100, random_state=42)
rf_model.fit(X_full, y_full)
rf_probs_full = rf_model.predict_proba(X_full)[:, 1]

# XGBoost
xgb_model = xgb.XGBClassifier(n_estimators=100, random_state=42, eval_metric='logloss')
xgb_model.fit(X_full, y_full)
xgb_probs_full = xgb_model.predict_proba(X_full)[:, 1]

# LightGBM
lgb_model = lgb.LGBMClassifier(n_estimators=100, random_state=42)
lgb_model.fit(X_full, y_full)
lgb_probs_full = lgb_model.predict_proba(X_full)[:, 1]

# CatBoost
cat_model = CatBoostClassifier(iterations=100, random_state=42, verbose=False)
cat_model.fit(X_full, y_full)
cat_probs_full = cat_model.predict_proba(X_full)[:, 1]

##############################################################################
# 6. STACKING META-MODEL
##############################################################################
stack_train_features = np.column_stack([
    rf_probs_full,
    xgb_probs_full,
    lgb_probs_full,
    cat_probs_full
])
meta_model = LogisticRegression()
meta_model.fit(stack_train_features, y_full)

train_preds = meta_model.predict(stack_train_features)
train_acc = accuracy_score(y_full, train_preds)
print(f"Stacked model training accuracy (on ALL data): {train_acc:.4f}")

##############################################################################
# 7. ASK FOR NEXT DAY'S OPEN PRICE & PREDICT
##############################################################################
predict_date = final_labeled_date + pd.Timedelta(days=1)
print(f"\nWe will now predict for => {predict_date.strftime('%Y-%m-%d')}")

open_str = input(f"Enter the OPEN price for {predict_date.strftime('%Y-%m-%d')}: ")
try:
    open_val = float(open_str)
except ValueError:
    sys.exit("Invalid numeric input. Exiting...")

# We'll approximate tomorrow's High=Low=Close = the same as Open
last_row = df_labeled.iloc[-1].copy()
tomorrow_row = last_row.copy()
tomorrow_row.name = predict_date

tomorrow_row['Open']  = open_val
tomorrow_row['High']  = open_val
tomorrow_row['Low']   = open_val
tomorrow_row['Close'] = open_val

# Build a DataFrame with shape (1, len(features))
X_tomorrow = pd.DataFrame([tomorrow_row[feature_cols].values],
                          columns=feature_cols,
                          index=[predict_date])

# Predict base model probabilities
rf_prob  = rf_model.predict_proba(X_tomorrow)[:, 1]
xgb_prob = xgb_model.predict_proba(X_tomorrow)[:, 1]
lgb_prob = lgb_model.predict_proba(X_tomorrow)[:, 1]
cat_prob = cat_model.predict_proba(X_tomorrow)[:, 1]

# Stack them
stack_live = np.column_stack([rf_prob, xgb_prob, lgb_prob, cat_prob])
final_live_prob = meta_model.predict_proba(stack_live)[:, 1]
final_live_pred = (final_live_prob > 0.5).astype(int)

print("\n=== FINAL PREDICTION ===")
print(f"Date: {predict_date.strftime('%Y-%m-%d')}")
print(f"Probability of Bullish Next Day: {final_live_prob[0]:.4f}")
print("Predicted Candle Direction:",
      "BULLISH" if final_live_pred[0] == 1 else "BEARISH")
